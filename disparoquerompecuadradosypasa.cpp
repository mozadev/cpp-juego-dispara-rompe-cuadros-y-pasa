void MoverTodo(int& xP, int& yP, int dxP, int dyP, char tecla, bool& estaDisparando, int& xB, int& yB, int dxB, int dyB, int**matriz)
{
	dxP = dyP = 0;
	if (kbhit())
	{
		tecla = getch();
		switch (tecla)
		{
		case 'W':
			dyP = -1;
			break;
		case 'S':
			dyP = 1;
			break;
		case 'D':
			dxP = 1;
			break;
		case 'A':
			dxP = -1;
			break;
		case 32:
			if (estaDisparando == false)
			{
				estaDisparando = true;
				xB = xP;
				yB = yP;
			}
			break;
		}
		if (matriz[yP + dyP][xP + dxP] == 1)
		{
			dxP = 0; dyP = 0;
		}
		xP = xP + dxP;
		yP = yP + dyP;
	}
	if (estaDisparando == true)
	{
		if (matriz[yB + dyB][xB + dxB] == 1)

			matriz[yB + dyB][xB + dxB] = 0;

		xB += dxB;
		yB += dyB;
		DibujarBala(xB, yB);
	}
	DibujarPersonaje(xP, yP);
	Sleep(100);
	BorrarPersonaje(xP, yP);
	if (estaDisparando == true)
	{
		BorrarBala(xB, yB);
	}
	if (yB <= 1)
	{
		estaDisparando = false;
	}
}
void Controlador(int& xP, int& yP, int dxP, int dyP, char tecla, bool& estaDisparando, int& xB, int& yB, int dxB, int dyB, int**matriz)
{
	mapa1(matriz);
	while (1)
	{
		MoverTodo(xP, yP, dxP, dyP, tecla, estaDisparando, xB, yB, dxB, dyB, matriz);
	}
}
